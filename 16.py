# comparison
print(5 == 5)
print(5 == '5')
print(5 > 5)
print(5 < 5)
print(5 >= 5)
num = 5
num *= 5
print(num)

num /= 2
print(num)

# logical
print('and')
print(True and True)
print(True and False)
print(10 > 5 and 100 > 10)

print('or')
print(True or True)
print(True or False or False)
print(10 > 5 or 100 < 10)


print('not')
print(not True or True)
print(not True or False)
print(not 10 > 5 or not 100 < 10)
print(not 10 > 5 or 100 < 10)
